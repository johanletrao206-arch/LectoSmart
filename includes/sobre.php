<h2 class="section-title">
Transformando la lectura en una experiencia interactiva
</h2>

<p class="section-lead">

LectoSmart es una plataforma web educativa diseñada para fortalecer las habilidades lectoras mediante actividades interactivas, recursos digitales y estrategias de gamificación.

Su propósito es mejorar la comprensión lectora, aumentar la motivación de los estudiantes y apoyar el trabajo de los docentes mediante herramientas tecnológicas modernas.

</p>

<p class="section-lead">

La plataforma incorpora ejercicios de comprensión, reconocimiento de palabras, lectura interactiva y seguimiento del progreso para ofrecer un aprendizaje personalizado y dinámico.

</p>

